<p><b>Jag tänkte skriva lite om designen på min sida innan jag besvarar frågorna.</b></p>
<p>Bland det första jag fixar på mina hemsidor är de (fula) blåa länkarna som är standard. Jag valde
en utstickande orange färg på både navbaren och länkarna, så de syns tydligt mot det annars neutrala
