<h3>Berätta om din syn/erfarenhet generellt kring communities och specifikt communities inom opensource
och programmeringsdomänen.</h3>
<p>Om dessa communities inte hade funnits tror jag inte jag hade kunnat klara någon av de kurser jag läst
hittills... Om jag fastnar på något och googlar mitt problem hittar jag i 99/100 fall alltid ett inlägg
på stackoverflow där någon haft exakt samma problem. Det finns jättemånga duktiga programmerare där ute
som flitigt är med och svarar på dessa foruminlägg och hjälper oss mindre kunnande.</p>
<p>Så jag tycker dessa communities förtjänar sin plats på internet och de har hjälpt mig och många andra
med att lösa problem enkelt och smidigt.</p>

<h3>Vad tror du om begreppet “en ramverkslös värld” som framfördes i videon?</h3>
<p>Jag tror inte att vi kommer se en helt ramverkslös värld inom en snar framtid, då man behöver både och
för att tillfredställa alla. De som inte är speciellt erfarna inom skapandet av hemsidor vill säkert
ha ett färdigt ramverk att bygga på, medan de riktigt kunniga säkert vill bygga från grunden utan att
känna sig begränsade av moduler, så som det beskrivs i videon. Jag tror själv att jag kommer gå mot den
ramverksfria delen i framtiden, just för att man kan skräddarsy mer och välja vilka delar man vill använda.</p>

<h3>Hur gick dina förberedelser inför kommentarssystemet?</h3>
