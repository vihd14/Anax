<h1>Om kursen Webbaserade Ramverk 1</h1>

<img class="figcenter" src="../htdocs/image/responsive-website-design.gif">


<p>Utdrag från kursförklaringen på dbwebb:</p>

<p class="quote"><i>"Kursen Webbaserade ramverk 1, a.k.a. ramverk1, lär ut programmering och kodstrukturer med ramverk,
designmönster och återanvändbara moduler samt tekniker för automatiserad testning och continuous
integration.i en fullstack webbmiljö."</i></p>

<p>Man kan alltså säga att ramverk1 är en fortsättning på kursen oophp, som vi läste i våras. Det bygger vidare
på uppbyggnaden av ramverk tillsammans med HTML, CSS (alt. SASS, LESS osv.) och PHP. Som i designkursen ingår
även lärandet av designmönster, samt återanvändning av befintliga moduler.</p>
